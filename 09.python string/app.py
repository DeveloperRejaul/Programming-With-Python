# python string 


# single quotes string 
x= 'hello world'; 

# double quotes string 
y= "hello world"; 

# single or double quotes string 
z= 'hello "world"'; 
print(z); 

# multiline sting single quoting  
text = ''' python is fun to learn '''; 

# multiline string double quoting 
text = """ python is fun to learn """ ; 

# concatenating string 
a= "hello"; 
b= "world"; 
print(a+b); 


# escaping characters in a string 
text = 'Let\'s learn python' ; 
print(text); 

# accessing parts of string 
text = "hello everyone"
print(text[0])
print(text[2])
print(text[-2])
print(text[-1])

# slicing string 
text = "python lover"; 
print(text[2:5]); 
print(text[2:])
print(text[-2:])