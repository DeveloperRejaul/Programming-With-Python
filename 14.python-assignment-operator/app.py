
# assignment operator
x = "hello"
y = 'world'

# addition assignment operator
x += y
print(x)

# subtraction operator
x = 20
y = 10
x -= y
print(x)

# multiplication assignment
x = 10
y = 15

x *= y
print(x)


# division assignment
x = 10
y = 2
x /= y
print(x)

# exponentiation assignment
x = 2
x **= 3
print(x)


# remainder Assignment
x = 5
x %= 2
print(x)

# floor division assignment
x = 5
x //= 2
print(x)
